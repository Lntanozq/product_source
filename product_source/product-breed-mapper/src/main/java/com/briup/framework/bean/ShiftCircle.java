package com.briup.framework.bean;

public class ShiftCircle {
    private Integer scId;

    private String scAnimalId;

    private String scOriginalHurdlesId;

    private String scNewHurdlesId;

    private String scTime;

    private String scUbreedId;

    private String scReason;

    public Integer getScId() {
        return scId;
    }

    public void setScId(Integer scId) {
        this.scId = scId;
    }

    public String getScAnimalId() {
        return scAnimalId;
    }

    public void setScAnimalId(String scAnimalId) {
        this.scAnimalId = scAnimalId;
    }

    public String getScOriginalHurdlesId() {
        return scOriginalHurdlesId;
    }

    public void setScOriginalHurdlesId(String scOriginalHurdlesId) {
        this.scOriginalHurdlesId = scOriginalHurdlesId;
    }

    public String getScNewHurdlesId() {
        return scNewHurdlesId;
    }

    public void setScNewHurdlesId(String scNewHurdlesId) {
        this.scNewHurdlesId = scNewHurdlesId;
    }

    public String getScTime() {
        return scTime;
    }

    public void setScTime(String scTime) {
        this.scTime = scTime;
    }

    public String getScUbreedId() {
        return scUbreedId;
    }

    public void setScUbreedId(String scUbreedId) {
        this.scUbreedId = scUbreedId;
    }

    public String getScReason() {
        return scReason;
    }

    public void setScReason(String scReason) {
        this.scReason = scReason;
    }
}