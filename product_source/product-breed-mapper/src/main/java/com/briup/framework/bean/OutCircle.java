package com.briup.framework.bean;

public class OutCircle {
    private Integer ocId;

    private String ocTime;

    private String ocBatchId;

    private String ocUbreedId;

    public Integer getOcId() {
        return ocId;
    }

    public void setOcId(Integer ocId) {
        this.ocId = ocId;
    }

    public String getOcTime() {
        return ocTime;
    }

    public void setOcTime(String ocTime) {
        this.ocTime = ocTime;
    }

    public String getOcBatchId() {
        return ocBatchId;
    }

    public void setOcBatchId(String ocBatchId) {
        this.ocBatchId = ocBatchId;
    }

    public String getOcUbreedId() {
        return ocUbreedId;
    }

    public void setOcUbreedId(String ocUbreedId) {
        this.ocUbreedId = ocUbreedId;
    }
}