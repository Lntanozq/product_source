package com.briup.framework.bean;

public class ColdStorageRecord {
    private Integer csrId;

    private String csrIntoTime;

    private String csrOutTime;

    private String csrAnimalId;

    private Integer csrColdId;

    private String csrUcoldId;

    public Integer getCsrId() {
        return csrId;
    }

    public void setCsrId(Integer csrId) {
        this.csrId = csrId;
    }

    public String getCsrIntoTime() {
        return csrIntoTime;
    }

    public void setCsrIntoTime(String csrIntoTime) {
        this.csrIntoTime = csrIntoTime;
    }

    public String getCsrOutTime() {
        return csrOutTime;
    }

    public void setCsrOutTime(String csrOutTime) {
        this.csrOutTime = csrOutTime;
    }

    public String getCsrAnimalId() {
        return csrAnimalId;
    }

    public void setCsrAnimalId(String csrAnimalId) {
        this.csrAnimalId = csrAnimalId;
    }

    public Integer getCsrColdId() {
        return csrColdId;
    }

    public void setCsrColdId(Integer csrColdId) {
        this.csrColdId = csrColdId;
    }

    public String getCsrUcoldId() {
        return csrUcoldId;
    }

    public void setCsrUcoldId(String csrUcoldId) {
        this.csrUcoldId = csrUcoldId;
    }
}