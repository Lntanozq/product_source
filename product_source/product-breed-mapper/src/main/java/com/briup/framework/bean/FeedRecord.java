package com.briup.framework.bean;

public class FeedRecord {
    private Integer frId;

    private String frReason;

    private String frTime;

    private String frBatchId;

    private String frUbreedId;

    private Integer frFeedId;

    public Integer getFrId() {
        return frId;
    }

    public void setFrId(Integer frId) {
        this.frId = frId;
    }

    public String getFrReason() {
        return frReason;
    }

    public void setFrReason(String frReason) {
        this.frReason = frReason;
    }

    public String getFrTime() {
        return frTime;
    }

    public void setFrTime(String frTime) {
        this.frTime = frTime;
    }

    public String getFrBatchId() {
        return frBatchId;
    }

    public void setFrBatchId(String frBatchId) {
        this.frBatchId = frBatchId;
    }

    public String getFrUbreedId() {
        return frUbreedId;
    }

    public void setFrUbreedId(String frUbreedId) {
        this.frUbreedId = frUbreedId;
    }

    public Integer getFrFeedId() {
        return frFeedId;
    }

    public void setFrFeedId(Integer frFeedId) {
        this.frFeedId = frFeedId;
    }
}